      <h2>MATERNAL AND CHILD HEALTH </h2>
      <h3>Integrated Management of Childhood Illnesses (IMCI) </h3>
      <p>
        In 2013, the Clinton Health Access Initiative (CHAI) partnered with Kenya Pediatric Research Consortium (KEPRECON) to support the Ministry of Health (MOH) strengthen the capacity of Health Care Workers (HCWs) in public and private institutions and clinics to effectively manage cases of childhood Pneumonia and Diarrhea. The partnership aims to reinforce knowledge and skills of HCW in the use of the Integrated Management of Childhood Illnesses (IMCI) technology with emphasis on diarrhea and pneumonia. This venture is driven by the current stalling of progress towards achieving the Millennium Development Goal (MDG) number 4 in the country and the realization that inappropriate utilization of interventions with established efficacy, such as IMCI by HCWs may be contributory to this state.  Current evidence still indicates that Pneumonia and Diarrhea are still among the top three causes of child deaths in Kenya. It has been estimated that over one third of Kenya’s child deaths occur in health facilities (personal communication from Kilifi) implying that effective health facility intervention remains pivotal in Kenya’s strive to achieve MDG 4.</p>

      <p>
        <b>Goal<br /></b>
        The goal of the partnership is to contribute to the reduction of the under-five mortality rate from 84/1000 to 33/1000 through improvement of diarrhea and pneumonia case management.  The first phase of the project will take place in Nairobi and coast regions.
      </p>

      <p>
        <b>Specific Objectives</b>
        <ol>
          <li>To refine the Diarrhea and Pneumonia orientation training curriculum presently adapted by the MOH.</li>
          <li>To establish robust teams of trainers of pneumonia/diarrhea management at the Coast and Nairobi.</li>  
          <li>To roll out comprehensive provider training to public and private health care providers in Nairobi &amp; Coast provinces.</li>
          <li>To develop diarrhea specific CMEs materials for use across the country</li>
        </ol>
      </p>

      <p>
        <b>Achievements</b>
        <ol>
          <li>A total of 427 health care providers have been trained in Integrated Management of Childhood Illnesses (IMCI) in the Coastal and Nairobi Regions from 14th October 2013 to 8th November 2013.</li>
          <li>Developed a Distance Learning Diarrhea Specific CME Tool and PowerPoint Presentation of the same.</li>
          <li>We have maintained  our  presence in strategic activities with  Division of Child Health  and other stakeholders </li>
        </ol>
      </p>
